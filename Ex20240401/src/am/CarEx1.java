package am;

public class CarEx1 {
	
	String model;
	int price;
	String maker;
	
	public void setModel(String newModel) {
		//인자를 받아서 model이라는 멤버변수에 저장한다.
		model = newModel;
	}

	public void setPrice(int newPrice) {
		//인자를 받아서 price이라는 멤버변수에 저장한다.
		price = newPrice;
	}
	
	public void setMaker(String newMaker) {
		//인자를 받아서 maker이라는 멤버변수에 저장한다.
		maker = newMaker;
	}

}
